###To compare the size of senior workers to junior workers in an organization
# In registering voters, this can be used to check and restrict registration for people under a certain age
# It can also be use to validate the correctness of the an inputted password 
# Also, to check for latecomers in an organisation###

#Using scenario 1 
seniorStaff = 34
juniorStaff = 98
print(f"{seniorStaff} == {juniorStaff} : {seniorStaff == juniorStaff}") #34 == 98 : False
print(f"{seniorStaff} != {juniorStaff} : {seniorStaff != juniorStaff}") #34 != 98 : True
print(f"{seniorStaff} > {juniorStaff} : {seniorStaff > juniorStaff}") #34 > 98 : False
print(f"{seniorStaff} < {juniorStaff} : {seniorStaff < juniorStaff}") #34 < 98 : True

